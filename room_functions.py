def get_rooms_info_from_api():  
    """Funci√≥n para obtener la informaci√≥n de los cuartos del endpoint de la API para el proyecto.

    Returns:
        list: informaci√≥n de los cuartos obtenida del endpoint de la API para el proyecto.
    """
    url = "https://api-escapamet.vercel.app/"

    response = requests.request("GET", url)

    rooms_info = response.json()

    return rooms_info

