def create_room_objects(items, images, games):
    """Funci√≥n para instanciar cada objeto (RoomObject) de un cuarto.

    Args:
        items (dict): diccionario con la informaci√≥n de cada objeto de un cuarto.
        images (list): lista de im√°genes de cada objeto de cuarto.
        games (list): lista de juegos de cada objeto.

    Returns:
        list: lista de objetos instanciados RoomObject de un cuarto.
    """
    
    objects = []

    for i, item in enumerate(items): #el orden de los elementos correspondientes a cada objeto en la lista de listas images es el mismo orden en el que aparecen los objetos en la lista items: por tanto, podemos usar el √≠ndice i para obtener la lista de im√°genes correspondientes a un objeto determinado. lo mismo ocurre con los juegos asociados a un objeto.
        room_object = RoomObject(item['name'], item['position'], images[i], games[i])
        objects.append(room_object)

    return objects

